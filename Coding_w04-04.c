#include <stdio.h>

int main() {
    int intscore = 90.5;   // กำหนดค่าทศนิยมให้กับตัวแปรชนิด int
    float floatscore = 90.5; // กำหนดค่าทศนิยมให้กับตัวแปรชนิด float

    // *** ข้อควรระวังตรงนี้: การใช้ %c กับ intscore จะแสดงผลเป็นตัวอักษร ASCII ไม่ใช่ตัวเลข 90 ***
    // หากต้องการแสดงผลเป็นตัวเลข ควรใช้ %d
    printf("Value (as char) = %c\n", intscore); // จะพิมพ์ตัวอักษร ASCII ที่มีค่า 90 (ซึ่งคือ 'Z')
    printf("Value (as int) = %d\n", intscore);   // แสดงค่า intscore เป็นตัวเลขจำนวนเต็มที่ถูกต้อง

    printf("Value (float) = %f\n", floatscore); // แสดงค่า floatscore เป็นตัวเลขทศนิยม
    
    return 0;
}

//--- การทดลองกับตัวแปรชนิด float ---
// ผลลัพธ์:
// เมื่อรันโปรแกรม บรรทัด printf สำหรับ float จะแสดงผล: Value (float) = 90.500000 (หรือใกล้เคียง)
// อภิปรายและสรุปผล:
// โปรแกรมคอมไพล์และรันได้ตามปกติ เพราะ 'float' เป็นชนิดข้อมูลที่ถูกออกแบบมาโดยเฉพาะเพื่อเก็บค่าตัวเลขที่มีจุดทศนิยม
// การเลือกใช้ Data Type ที่ตรงกับลักษณะข้อมูลที่เราต้องการจัดเก็บ (เช่น 'float' สำหรับค่า 90.5) ทำให้ข้อมูลถูกเก็บอย่างถูกต้อง
// และสามารถนำไปแสดงผลหรือประมวลผลต่อได้โดยไม่เกิดการคลาดเคลื่อน หากสะกดชื่อชนิดข้อมูลผิด (เช่น 'flot') จะทำให้โปรแกรมคอมไพล์ไม่ผ่านเลย (Syntax Error)
//
//--- การทดลองกับตัวแปรชนิด int ---
// ผลลัพธ์ที่ได้:
// เมื่อรันโปรแกรม บรรทัด printf สำหรับ intscore ด้วย %d จะแสดงผล: Value (as int) = 90
// (ส่วนบรรทัดที่ใช้ %c จะแสดง 'Z' ซึ่งเป็นค่า ASCII)
// อภิปรายและสรุปผล:
// เมื่อเรากำหนดค่าที่มีทศนิยม (90.5) ให้กับตัวแปรชนิด 'int' ส่วนทศนิยม (.5) จะถูก "ตัดทิ้ง" (truncated) โดยอัตโนมัติ
// ส่งผลให้ตัวแปร 'intscore' เก็บได้เพียงค่าจำนวนเต็มคือ 90 เท่านั้น
// ดังนั้น การเลือก Data Type ที่ไม่เหมาะสม (เช่น ใช้ 'int' กับค่าทศนิยม) จะทำให้ข้อมูลสูญหาย และโปรแกรมแสดงผลลัพธ์ไม่ตรงตามความตั้งใจเดิม ซึ่งส่งผลต่อความถูกต้องแม่นยำของข้อมูลอย่างมาก
//
//--- สรุปบทเรียนโดยรวม ---
// จากการทดลองทั้งสองกรณี เราได้เห็นความสำคัญอย่างยิ่งของการ "เลือกชนิดข้อมูลให้ถูกต้อง"
// 1. ชนิดข้อมูล `int` เหมาะสำหรับเก็บ "จำนวนเต็ม" เท่านั้น การพยายามเก็บค่าทศนิยมจะทำให้ส่วนทศนิยมถูกตัดทิ้ง
// 2. ชนิดข้อมูล `float` (และ `double`) เหมาะสำหรับเก็บ "ตัวเลขทศนิยม" ทำให้สามารถรักษาความแม่นยำของค่าได้
// การเลือกชนิดข้อมูลที่ไม่เหมาะสมกับประเภทของค่าที่เราต้องการใช้งาน อาจทำให้เกิดการสูญเสียข้อมูล (Data Loss) หรือทำให้โปรแกรมทำงานและแสดงผลลัพธ์ไม่ตรงกับความคาดหวังได้
// การเข้าใจและเลือกใช้ Data Type ที่ถูกต้องจึงเป็นพื้นฐานสำคัญในการเขียนโปรแกรมที่มีความแม่นยำและน่าเชื่อถือ