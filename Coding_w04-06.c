#include <stdio.h>

//ประกาศค่าคงที่ระดับ Global
const int GLOBAL_RATE = 10;

void calculate() {
    //ประกาศค่าคงที่ระดับ Local
    const int LOCAL_BONUS = 50;
    //แสดงค่าทั้งสอง
    printf("GLOBAL_RATE ใน calculate() = %d\n", GLOBAL_RATE);
    printf("LOCAL_BONUS ใน calculate() = %d\n", LOCAL_BONUS);
}

int main () {
    calculate();
    //แสดงค่าคงที่ Global
    printf("GLOBAL_RATE ใน main() = %d\n", GLOBAL_RATE);

    // *** บรรทัดนี้คือส่วนที่ 'จะทำให้เกิด compile error' หากยกเลิกคอมเมนต์ (uncomment) ***
    // เพราะ LOCAL_BONUS ถูกประกาศในฟังก์ชัน calculate() เท่านั้น ไม่สามารถเรียกใช้ที่นี่ใน main() ได้
    // printf("LOCAL_BONUS ใน main() = %d\n", LOCAL_BONUS);

    return 0;
}

//   ค่าคงที่ GLOBAL_RATE และ LOCAL_BONUS ต่างกันอย่างไร?
//   GLOBAL_RATE มี "ขอบเขตแบบ Global" คือเป็นที่รู้จักและเข้าถึงได้จากทุกส่วนของโปรแกรม
//   ส่วน LOCAL_BONUS มี "ขอบเขตแบบ Local" คือเป็นที่รู้จักและเข้าถึงได้เฉพาะภายในฟังก์ชัน calculate() ที่มันถูกประกาศเท่านั้น
//
//   เกิดอะไรขึ้นถ้าเราเขียน GLOBAL_RATE = 20; หรือ LOCAL_BONUS = 80;?
//   จะเกิด "ข้อผิดพลาดตอนคอมไพล์" (Compile-time Error) ทันที โปรแกรมจะไม่สามารถสร้างเป็นไฟล์ที่รันได้
//   เพราะคำว่า 'const' แปลว่า "ค่าคงที่" ซึ่งเมื่อกำหนดค่าไปแล้วจะไม่สามารถเปลี่ยนแปลงได้อีก
//
//   ทำไม LOCAL_BONUS ถึงไม่สามารถเรียกใช้ใน main() ได้? อภิปราย และสรุปผลที่ได้จากการทดลอง
//   LOCAL_BONUS ถูกสร้างขึ้นมาและมีชีวิตอยู่ "ภายใน" ฟังก์ชัน calculate() เท่านั้น มันจึงไม่เป็นที่รู้จักนอกฟังก์ชันนั้น
//   เมื่อเราพยายามเรียกใช้ LOCAL_BONUS จาก main() คอมไพเลอร์จะไม่พบตัวแปรนี้ในขอบเขตของ main() และจะแจ้งข้อผิดพลาดว่า "undeclared identifier" (ตัวแปรนี้ไม่เคยถูกประกาศในที่นี้)
//
//สรุป
//- 'const' ใช้เพื่อประกาศตัวแปรที่มีค่า "คงที่" ซึ่งไม่สามารถเปลี่ยนแปลงได้หลังจากการกำหนดค่าเริ่มต้น
//- ตัวแปรหรือค่าคงที่ที่ประกาศนอกฟังก์ชันใดๆ (Global) สามารถใช้งานได้จากทุกๆ ส่วนของโค้ด
//- ตัวแปรหรือค่าคงที่ที่ประกาศภายในฟังก์ชันหรือบล็อกโค้ด (Local) จะใช้งานได้เฉพาะในขอบเขตนั้นๆ เท่านั้น
//- การพยายามเปลี่ยนแปลงค่าของตัวแปร 'const' หรือพยายามเข้าถึงตัวแปร local จากนอกขอบเขต จะทำให้เกิด "Compile-time Error" (โปรแกรมคอมไพล์ไม่ผ่าน)